// pode inverter o valor booleano de um elemento. 
// apenas usando ! podemos trocar o valor de true para false e vice versa .

//exemplo 01 Strings

const squirtle = "melhor pokemon inicial";

console.log(!squirtle); // false

//exemplo 01 Números

console.log(!42); // false

console.log(!0); // true
// O número 0 tem o valor "falsy" no javascript. Logo, seu oposto é true.

// Valores nulos...
console.log(!null); // true

// Valores indefinidos..

console.log(!undefined); // true
